package ejercicioA1.java;

public class Punto {
	
	//Atributo

	private int x;
	private int y;
	
	//Constructor
	
	public Punto(int x, int y) {
		
		this.x = x;
		this.y = y;
		
	}
	
	//Metodos
	
	public int getX() {
		
		return this.x;
		
	}
	
	public int getY() {
		
		return this.x;
		
	}
	
	public void setX(int x) {
		
		this.x = x;
		
	}
	
	public void setY(int y) {
		
		this.x = y;
		
	}
	
}
